public class OperatorEqualityandRelational {
    public static void main(String[] args) {
        System.out.println(5==5);
        System.out.println(5==6);
        System.out.println(5>6);
        System.out.println(5>=5);
        System.out.println(5<=6);
        System.out.println(5<6);
        System.out.println(5!=5);
        System.out.println(5=='j');
        System.out.println('i'=='i');
    }
        
}
