public class OperatorUnary {
    public static void main(String[] args) {
        int i = 0;
        
//        i = i + 3
        i+=3;
        System.out.println(i);
        // i = i * 3
        i*=3;
        System.out.println(i);               
        // i = i - 3
        i-=3;
        System.out.println(i);
        // i =i / 3
        i/=3;
        System.out.println(i);           
    }
}
